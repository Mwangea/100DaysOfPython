# Nested if statement example

# Define variables
is_raining = True
have_umbrella = False

if is_raining:
    print("It's raining!")

    if have_umbrella:
        print("You have an umbrella. Stay dry!")
    else:
        print("You don't have an umbrella. Stay indoors.")

else:
    print("It's not raining. Enjoy the weather!")
